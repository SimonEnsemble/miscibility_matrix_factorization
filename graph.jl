begin
	df = CSV.read("pairs.csv", DataFrame, header=0)
	
	M_complete = Int.(Array(df)) # miscibility matrix
	n_compounds = size(M_complete)[1]
	compounds = CSV.read("compounds.csv", DataFrame)
	sort!(compounds, :CLASS)
	compound_classes = String.(compounds[:, "CLASS"])
end

# ╔═╡ bc2bf573-b609-496d-8be5-9741508d0405
begin
	K = zeros(n_compounds, n_compounds)
	for i = 1:n_compounds
		for j = 1:n_compounds
			if i == j
				K[i, j] = 0 
			else
				K[i, j] = compound_classes[i] == compound_classes[j]
			end
		end
	end
end
	

# ╔═╡ f9d05204-d608-4de8-a9f8-d352e18c592d
graph = SimpleGraph(K)

# ╔═╡ a82b6265-12e2-4a1a-8e44-de08742dbfcb
begin
	nodelabel = 1:nv(graph)
	layout=(args...)->spring_layout(args...; C=8)
	gplot(graph, layout=layout, nodelabel = nodelabel)
	
end

